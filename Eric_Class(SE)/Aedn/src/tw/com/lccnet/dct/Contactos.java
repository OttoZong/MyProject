package tw.com.lccnet.dct;

public class Contactos{
	private Integer id;
	private String nomberes;
	private Integer celular;
	private String corred;
	
	public Contactos() {
		
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getNomberes() {
		return nomberes;
	}

	public void setNomberes(String nomberes) {
		this.nomberes = nomberes;
	}

	public Integer getCelular() {
		return celular;
	}

	public void setCelular(Integer celular) {
		this.celular = celular;
	}

	public String getCorred() {
		return corred;
	}

	public void setCorred(String corred) {
		this.corred = corred;
	}
	
	
}
