package Day01;

public class String_toUpperCase_toLowerCase {

	public static void main(String[] args) {
		String str = "我愛Java";
		String upper = str.toUpperCase();
		//將英文字串轉為大寫
		System.out.println("upper:"+upper);
		
		
		String lower = str.toLowerCase();
		//將英文字串轉為小寫
		System.out.println("lower:"+lower);

	}

}
